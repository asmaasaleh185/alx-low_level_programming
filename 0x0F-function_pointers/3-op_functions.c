#include "3-calc.h"
/**
 * op_add - adds two int
 * @a: input
 * @b: input
 * Return: sum
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - substracts two int
 * @a: input
 * @b: input
 * Return: the substraction
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_div - divides two int
 * @a: input
 * @b: input
 * Return: division
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - module two int
 * @a: input
 * @b: input
 * Return: reminder
 */
int op_mod(int a, int b)
{
	return (a % b);
}

/**
 * op_mul - multiplies two int
 * @a: input
 * @b: input
 * Return: product
 */
int op_mul(int a, int b)
{
	return (a * b);
}
